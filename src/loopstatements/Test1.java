package loopstatements;

public class Test1 {

	public static void main(String[] args) {
		for (int i = 1; i <= 20; i++) {
			System.out.println(i);
		}
		System.out.println("************");
		System.out.println("after reversing");
		System.out.println("************");

		for (int i = 20; i >= 1; i--) {
			System.out.println(i);
		}

	}

}
